import UIKit

/// Colors added as assets for ease of access
extension UIColor {
    static var linearGradientStartColor: UIColor {
        return UIColor(named: "linearGradientStart") ?? .black
    }
    
    static var linearGradientEndColor: UIColor {
        return UIColor(named: "linearGradientEnd") ?? .black
    }
}
